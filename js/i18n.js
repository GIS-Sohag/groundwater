    _translateAll() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const val = this.dict[key];
            if (!val) return;

            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.setAttribute('placeholder', val);
                return;
            }

            let hasIcon = el.querySelector('i, svg');
            if (hasIcon) {
                let textNodeFound = false;
                for (let i = 0; i < el.childNodes.length; i++) {
                    const node = el.childNodes[i];
                    if (node.nodeType === Node.TEXT_NODE && node.textContent.trim().length > 0) {
                        node.textContent = (this.lang === 'en' ? ' ' : '') + val + ' ';
                        textNodeFound = true;
                        break;
                    }
                }
                if (!textNodeFound) {
                    const textNode = document.createTextNode((this.lang === 'en' ? ' ' : '') + val);
                    el.appendChild(textNode);
                }
            } else {
                el.textContent = val;
            }
        });

        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            const val = this.dict[el.getAttribute('data-i18n-html')];
            if (val) el.innerHTML = val;
        });

        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const val = this.dict[el.getAttribute('data-i18n-title')];
            if (val) el.setAttribute('title', val);
        });
    }

